package org.whole.lang.ui.commands;

import org.eclipse.gef.commands.Command;
import org.whole.lang.ui.ruler.WholeGuide;
import org.whole.lang.ui.ruler.WholeRuler;

/**
 * @author Riccardo Solmi
 */
public class CreateGuideCommand extends Command implements ILegacyCommand {
	private WholeGuide guide;
	private WholeRuler parent;
	private int position;

	public CreateGuideCommand(WholeRuler parent, int position) {
		super("Create guide");
		this.parent = parent;
		this.position = position;
	}

	public boolean canUndo() {
		return true;
	}

	public void execute() {
		if (guide == null)
			guide = new WholeGuide(!parent.isHorizontal());
		guide.setPosition(position);
		parent.addGuide(guide);
	}

	public void undo() {
		parent.removeGuide(guide);
	}

}
